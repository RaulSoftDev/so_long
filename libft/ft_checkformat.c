#include "libft.h"

int	ft_checkformat(char c)
{
	if (c != 'c' && c != 's' && c != 'i'
		&& c != 'd' && c != 'u' && c != 'x'
		&& c != 'X' && c != 'p')
		return (0);
	else
		return (1);
}
